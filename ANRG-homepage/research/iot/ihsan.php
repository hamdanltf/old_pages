                                dengan cara mendekatkan kartu RFID kepada RFID reader. Selain untuk membuka kunci
                                pintu, RFID ini juga bertujuan untuk mengupdate status keadaan didalam rumah apakah ada
                                orang atau tidak. Sehingga saat ada tamu yang datang, tamu akan diberitahu bahwa ada
                                seseorang di rumah dan akan diminta untuk menunggu melalui display yang ada pada
                                rangkaian RFID. User pun selanjutnya akan diberitahu melalui notifikasi di

                                <li>Start</li>
                                <p>Langkah pertama adalah dengan menghubungkan perangkat user ke jaringan internet.</p>
                                <li>Login Aplikasi</li>
                                <p>Setelah perangkat tersambung dengan internet, user diharapkan untuk login terlebih
                                    dahulu ke aplikasi agar terhindari dari oknum jahat yang ingin mensabotase sistem.
                                </p>
                                <li>Verifikasi Data</li>
                                <p>Tahap ini akan melakukan otentikasi terhadap data yng dimasukan oleh user jika ya
                                    maka tahap akan berlanjut namun jika tidak tahap akan mengulang ke Login aplikasi.
                                </p>
                                <li>User Mengontrol</li>
                                <p>Pada tahapan ini user sudah memasuki aplikasi dan dapat mengontrol pintu melalui
                                    aplikasi tersebut.</p>
                                <li>Pemberitahuan Masuk</li>
                                <p>Karena pada diagram alir ini terdapat kasus dimana rumah kedatngan seorang tamu,
                                    maka kita masukan tahap dimana notifikasi masuk ke perangkat user yang memberitahu
                                    bahwa seorang tamu sedang berada di depan. Jika memang terdapat notifikasi yang
                                    masuk maka tahapan akan berlanjut kepada pengenalan tamu. Sedangkan apabila tidak
                                    maka akan kembali ke tahapan user mengontrol.</p>
                                <li>User Melihat Foto Melalui Aplikasi</li>
                                <p>Setelah notifikasi masuk, user dapat melihat siapakah tamu yang sedang berada
                                    didepan pintu. Dengan cara melihat foto di menu pada aplikasi yang diambil saat
                                    tamu menekan tombol door bell.</p>
                                <li>Mengenali Tamu</li>
                                <p>Jika user mengenali tamu yang berada di depan pintu maka user dapat membukanya tanpa
                                    perlu mendatangi pintu tersebut melalui kontrol yang terdpat didalam aplikasi.
                                    Namun, jika user tidak mengenali tamu maka dapat dibiarkan saja dan tahap akan
                                    langsung menuju END.</p>
                                <li>Akses Pintu</li>
                                <p>Selanjutnya saat user mengenali tamu, user akan memulai akses pintu dengan aplikasi.
                                </p>
                                <li>Pintu Lock</li>
                                <p>Tahapan ini akan melihat apakah pintu dalam keadaan terkunci atau tidak. Jika tidak
                                    maka tamu dapat langsung masuk dan tahapan akan menuju END, tetapi jika tidak maka
                                    user akan memilih opsi unlock untuk membuka kunci pintu.</p>
                                <li>Solenoid ON</li>
                                <p>Perintah unlock akan memicu aktifnya relai. Aliran listrik pun sampai ke solenoid
                                    dan mengaktifkan solenoid tersebut sehingga besi tertarik kedalam dan pintu dapat
                                    dibuka</p>
                                <li>Solenoid OFF</li>
                                <p>Setelah selang beberapa waktu (tergantung berapa waktu yang dimasukan pada program)
                                    solenoid akan kembali mati yang menyebabkan pintu kembali dalam posisi terkunci</p>
                                <li>User OFF</li>
                                <p>Pada tahapan ini akan dilihat apakah user Log out atau keluar dari aplikasi, jika ya
                                    maka tahapan akan berlanjut ke END namun jika tidak maka tahapan akan mengulang ke
                                    user mengontrol.</p>

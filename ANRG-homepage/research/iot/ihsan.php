                                kunci dapat digunakan secara remote melalui internet. Sebaliknya saat keluar rumah pun
                                user diminta untuk mengunci pintu melalui RFID karena nantinya sitem akan mengupdate
                                status dari rumah bahwa tidak ada orang di rumah. Seingga nantinya saat ada tamu yang
                                datang, tamu akan diminta untuk kembali lain waktu karena di rumah sedang tidak ada
                                orang.</p>

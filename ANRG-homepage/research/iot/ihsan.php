                                tersebut juga terdapat sistem Internet of Things sehingga kunci pintu dapat dibuka
                                secara remote. Sebagai pengganti kunci konvensional, sistem menggunakan RFID card yang
                                merupakan smartcard yang didalamnya terdapat tag berupa kode unik yang berbeda setiap
                                kartunya. Sehingga kunci hanya dapat terbuka oleh kartu yang dikenali saja. Sistem akan
                                memberikan notifikasi kepada pemilik rumah jika ada seseorang yang tidak dikenal
                                membuka kunci secara paksa.</p>

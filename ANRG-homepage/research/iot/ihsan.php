                    <div class="row judul justify-content-center">
                        <h1>Diagram Alir RFID</h1>
                    </div>
                    <hr>
                    <div class="row isi text-justify">
                        <div class="col-lg-6">
                            <div class="card card border-white mb-3">
                                <img class="card-img-top" src="../../img/research/iot/ihsan/diagram-alir-rfid.jpg"
                                    alt="Card image cap">
                                <figcaption class="figure-caption text-center">Diagram Alir RFID</figcaption>
                            </div>
                        </div>
                        <div class="col-lg-6">
                            <ol type="1">
                                <li>Start</li>
                                <p>Langkah pertama untuk mengoperasikan alat adalah dengan memberikan tegangan pada
                                    sistem atau rangkaian.</p>
                                <li>Inisiasi mikrokontroler</li>
                                <p>Setelah sistem aktif, Raspberry Pi akan melakukan fungsinya sebagai kontrol dari
                                    semua input dan output. Raspberry pi mengaktifkan RFID reader dan LCD. Setelah
                                    aktif, Raspberry pi LCD akan menampilkan tulisan untuk menempelkan Kartu dan secara
                                    berkala mengambil data dari Firebase.</p>
                                <li>RFID Reader melakukan scan kartu</li>
                                <p>RFID reader akan membaca UID kartu melalui pancaran gelombang elektromagnetik. UID
                                    yang terbaca oleh RFID reader akan diteruskan ke Raspberry pi untuk divalidasi
                                    dengan data yang diambil dari Firebase. </p>
                                <li>Validasi Kartu</li>
                                <p>Apabila UID yang dikirim oleh RFID reader bernilai valid (sesuai dengan database)
                                    Raspberry pi akan menjalankan instruksi selanjutnya yaitu menampilakan permintaan
                                    masukan password dan mengaktifkan counter.</p>
                                <li>UID tak terdaftar</li>
                                <p>Apabila kartu yang ditempelkan tidak sesuai, maka UID yang ditempelkan tidak
                                    terdaftar pada database Firebase dan LCD akan menampilkan tulisan “Access Denied”
                                </p>
                                <li>LED menyala merah dan Buzzer berbunyi</li>
                                <p>LED merah akan menyala dan buzzer akan berbunyi sebagai tanda UID tidak terdaftar
                                    pada database UID</p>
                                <li>Display “Enter Password” dan Counter = On</li>
                                <p>Pengguna dapat memasukan password. Counter adalah banyaknya kesempatan pengguna
                                    dapat memasukan password.</p>
                                <li>Counter < 4</li> <p>Setiap pengguna menekan tombol “#” yang berarti validasi
                                        password, makan counter akan bertambah 1. Sehingga pengguna hanya diberikan
                                        kesempatan sebanyak 3 kali untuk memasukan password. </p>
                                <li>Verifikasi Password</li>
                                <p>Password yang dimasukan oleh pengguna akan dicocokan oleh sistem dengan data UID
                                    yang sesuai.</p>
                                <li>Display “Please Enter”, Solenoid On</li>
                                <p>Saat Solenoid dalam keadaan “On” maka batangan pengunci akan tertarik ke dalam dan
                                    pintu dapat dibuka. </p>
                                <li>Pintu Terbuka</li>
                                <p>Setelah relai aktif. Aliran listrik pun sampai ke solenoid dan mengaktifkan solenoid
                                    tersebut sehingga besi tertarik kedalam dan pintu dapat dibuka. </p>
                                <li>Solenoid OFF</li>
                                <p>Setelah selang beberapa waktu (tergantung berapa waktu yang dimasukan pada program)
                                    solenoid akan kembali mati yang menyebabkan pintu kembali dalam posisi terkunci.</p>
                            </ol>

                                Bell, RFID Reader, Web Camera, dan Camera Module. Dan juga keluaran sebanyak 4 buah
                                yaitu Display, Relay yang terhubung dengan solenoid, LED, dan IoT platform yang
                                terhubung dengan aplikasi untuk memberikan notifikasi. Dan mikrokontroler yang kedua
